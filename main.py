hours = input('Enter your money per hour : ')
day = 8 * int(hours)
week = day * 5
month = week * 4
year = month * 12

print('Money per hour : ' + str(hours) + ' $')
print('Money per day ' + str(day) + ' $')
print('Money per week : ' + str(week) + ' $')
print('Money per month : ' + str(month) + ' $')
print('Money per year :' + str(year) + ' $')
